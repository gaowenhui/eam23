package com.tansun.eam2.common.word.components;

public class WordTemplate {

	String id;
	
	String wordTemplateID;
	
	String fieldMapping;
	
	String extJSFunction;
	
	
		
	public String getExtJSFunction() {
		return extJSFunction;
	}
	public void setExtJSFunction(String extJSFunction) {
		this.extJSFunction = extJSFunction;
	}
	public String getFieldMapping() {
		return fieldMapping;
	}
	public void setFieldMapping(String fieldMapping) {
		this.fieldMapping = fieldMapping;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	} 
	public String getWordTemplateID() {
		return wordTemplateID;
	}
	public void setWordTemplateID(String wordTemplateID) {
		this.wordTemplateID = wordTemplateID;
	}
	
	
}
